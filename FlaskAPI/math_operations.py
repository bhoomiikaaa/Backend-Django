def add(a, b):
    return a + b


def subtract(a, b):
    return a - b


def multiply(a, b):
    return a * b


def divide(a, b):
    return a / b


if __name__ == "__main__":
    print("Addition:", add(1, 2))
    print("Subtraction:", subtract(10, 5))
    print("Multiplication:", multiply(3, 4))
    print("Division:", divide(12, 6))
